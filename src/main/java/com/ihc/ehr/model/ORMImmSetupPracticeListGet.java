package com.ihc.ehr.model;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity
public class ORMImmSetupPracticeListGet {

	@Id
	private Long practice_immunization_id;
	private String cvx_code;
	private String immunization_name;
	private String status;

	public Long getPractice_immunization_id() {
		return practice_immunization_id;
	}

	public void setPractice_immunization_id(Long practice_immunization_id) {
		this.practice_immunization_id = practice_immunization_id;
	}

	public String getCvx_code() {
		return cvx_code;
	}

	public void setCvx_code(String cvx_code) {
		this.cvx_code = cvx_code;
	}

	public String getImmunization_name() {
		return immunization_name;
	}

	public void setImmunization_name(String immunization_name) {
		this.immunization_name = immunization_name;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

}
