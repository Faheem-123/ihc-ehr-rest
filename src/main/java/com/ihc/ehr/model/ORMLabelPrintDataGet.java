package com.ihc.ehr.model;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity
public class ORMLabelPrintDataGet {
	
	@Id
	Long id;
	String pid;
	String patient_name;
	String dob;
	String phone;
	String address_line1;
	String address_line2;
	String city;
	String state;
	String zip;
	String collection_start_datetime;
	String collection_end_datetime;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getPid() {
		return pid;
	}
	public void setPid(String pid) {
		this.pid = pid;
	}
	public String getPatient_name() {
		return patient_name;
	}
	public void setPatient_name(String patient_name) {
		this.patient_name = patient_name;
	}
	public String getDob() {
		return dob;
	}
	public void setDob(String dob) {
		this.dob = dob;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getAddress_line1() {
		return address_line1;
	}
	public void setAddress_line1(String address_line1) {
		this.address_line1 = address_line1;
	}
	public String getAddress_line2() {
		return address_line2;
	}
	public void setAddress_line2(String address_line2) {
		this.address_line2 = address_line2;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getZip() {
		return zip;
	}
	public void setZip(String zip) {
		this.zip = zip;
	}
	public String getCollection_start_datetime() {
		return collection_start_datetime;
	}
	public void setCollection_start_datetime(String collection_start_datetime) {
		this.collection_start_datetime = collection_start_datetime;
	}
	public String getCollection_end_datetime() {
		return collection_end_datetime;
	}
	public void setCollection_end_datetime(String collection_end_datetime) {
		this.collection_end_datetime = collection_end_datetime;
	}
	
	
}
