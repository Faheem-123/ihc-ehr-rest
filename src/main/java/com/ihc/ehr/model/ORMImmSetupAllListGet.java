package com.ihc.ehr.model;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity
public class ORMImmSetupAllListGet {

	@Id
	private Long immunization_id;
	private String cvx_code;
	private String immunization_name;
	private String long_name;
	private String note;
	private String status;

	public Long getImmunization_id() {
		return immunization_id;
	}

	public void setImmunization_id(Long immunization_id) {
		this.immunization_id = immunization_id;
	}

	public String getCvx_code() {
		return cvx_code;
	}

	public void setCvx_code(String cvx_code) {
		this.cvx_code = cvx_code;
	}

	public String getImmunization_name() {
		return immunization_name;
	}

	public void setImmunization_name(String immunization_name) {
		this.immunization_name = immunization_name;
	}

	public String getLong_name() {
		return long_name;
	}

	public void setLong_name(String long_name) {
		this.long_name = long_name;
	}

	public String getNote() {
		return note;
	}

	public void setNote(String note) {
		this.note = note;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

}
