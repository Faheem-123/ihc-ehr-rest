package com.ihc.ehr.model;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity
public class ORMGetSuperBillCategoryList {
	
	@Id
	private Long category_id;
	private String category_name;
	private String code1_type;
	private String code2_type;
	public Long getCategory_id() {
		return category_id;
	}
	public void setCategory_id(Long category_id) {
		this.category_id = category_id;
	}
	public String getCategory_name() {
		return category_name;
	}
	public void setCategory_name(String category_name) {
		this.category_name = category_name;
	}
	public String getCode1_type() {
		return code1_type;
	}
	public void setCode1_type(String code1_type) {
		this.code1_type = code1_type;
	}
	public String getCode2_type() {
		return code2_type;
	}
	public void setCode2_type(String code2_type) {
		this.code2_type = code2_type;
	}
}
