package com.ihc.ehr.model;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity
public class ORMPatientInsurancesForMergeGet {

	@Id
	private Long patientinsurance_id;
	private String name;
	private String address;	
	private String insurace_type;
	private String policy_number;
	private String group_number;	
	private Long patient_id;
	private String patient_name;
	public Long getPatientinsurance_id() {
		return patientinsurance_id;
	}
	public void setPatientinsurance_id(Long patientinsurance_id) {
		this.patientinsurance_id = patientinsurance_id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getInsurace_type() {
		return insurace_type;
	}
	public void setInsurace_type(String insurace_type) {
		this.insurace_type = insurace_type;
	}
	public String getPolicy_number() {
		return policy_number;
	}
	public void setPolicy_number(String policy_number) {
		this.policy_number = policy_number;
	}
	public String getGroup_number() {
		return group_number;
	}
	public void setGroup_number(String group_number) {
		this.group_number = group_number;
	}
	public Long getPatient_id() {
		return patient_id;
	}
	public void setPatient_id(Long patient_id) {
		this.patient_id = patient_id;
	}
	public String getPatient_name() {
		return patient_name;
	}
	public void setPatient_name(String patient_name) {
		this.patient_name = patient_name;
	}
	
	
}
