package com.ihc.ehr.model;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity
public class ORMRptDailyPaymentSummary {


	@Id
	private Long sno;
	private String summary_date;	
	private String patient_cash;
	private String patient_check;
	private String patient_cc;
	private String sub_total_patient;
	private String era_payment;
	private String eob_payment;
	private String sub_total_insurance;
	private String total;
	private Boolean is_aggregate;
	public Long getSno() {
		return sno;
	}
	public void setSno(Long sno) {
		this.sno = sno;
	}
	public String getSummary_date() {
		return summary_date;
	}
	public void setSummary_date(String summary_date) {
		this.summary_date = summary_date;
	}
	public String getPatient_cash() {
		return patient_cash;
	}
	public void setPatient_cash(String patient_cash) {
		this.patient_cash = patient_cash;
	}
	public String getPatient_check() {
		return patient_check;
	}
	public void setPatient_check(String patient_check) {
		this.patient_check = patient_check;
	}
	public String getPatient_cc() {
		return patient_cc;
	}
	public void setPatient_cc(String patient_cc) {
		this.patient_cc = patient_cc;
	}
	public String getSub_total_patient() {
		return sub_total_patient;
	}
	public void setSub_total_patient(String sub_total_patient) {
		this.sub_total_patient = sub_total_patient;
	}
	public String getEra_payment() {
		return era_payment;
	}
	public void setEra_payment(String era_payment) {
		this.era_payment = era_payment;
	}
	public String getEob_payment() {
		return eob_payment;
	}
	public void setEob_payment(String eob_payment) {
		this.eob_payment = eob_payment;
	}
	public String getSub_total_insurance() {
		return sub_total_insurance;
	}
	public void setSub_total_insurance(String sub_total_insurance) {
		this.sub_total_insurance = sub_total_insurance;
	}
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	public Boolean getIs_aggregate() {
		return is_aggregate;
	}
	public void setIs_aggregate(Boolean is_aggregate) {
		this.is_aggregate = is_aggregate;
	}
	
	
	
}
