package com.ihc.ehr.model;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity
public class ORMGetAutidLogQuery {
	
	@Id
	private Long query_id;
	private String log_query;
	private String query_order;
	private String log_headers;
	private String log_table_alias;
	public Long getQuery_id() {
		return query_id;
	}
	public void setQuery_id(Long query_id) {
		this.query_id = query_id;
	}
	public String getLog_query() {
		return log_query;
	}
	public void setLog_query(String log_query) {
		this.log_query = log_query;
	}
	public String getQuery_order() {
		return query_order;
	}
	public void setQuery_order(String query_order) {
		this.query_order = query_order;
	}
	public String getLog_headers() {
		return log_headers;
	}
	public void setLog_headers(String log_headers) {
		this.log_headers = log_headers;
	}
	public String getLog_table_alias() {
		return log_table_alias;
	}
	public void setLog_table_alias(String log_table_alias) {
		this.log_table_alias = log_table_alias;
	}	
}
