package com.ihc.ehr.model;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity
public class ORMGetRoleCDS {

	@Id
	private Long rule_id;
	private Long id;	
	private Long role_id;
    private String rule_name;
    private Boolean is_active_role;
    private String show_reference_link;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getRule_id() {
		return rule_id;
	}
	public void setRule_id(Long rule_id) {
		this.rule_id = rule_id;
	}
	public Long getRole_id() {
		return role_id;
	}
	public void setRole_id(Long role_id) {
		this.role_id = role_id;
	}
	public String getRule_name() {
		return rule_name;
	}
	public void setRule_name(String rule_name) {
		this.rule_name = rule_name;
	}
	public Boolean getIs_active_role() {
		return is_active_role;
	}
	public void setIs_active_role(Boolean is_active_role) {
		this.is_active_role = is_active_role;
	}
	public String getShow_reference_link() {
		return show_reference_link;
	}
	public void setShow_reference_link(String show_reference_link) {
		this.show_reference_link = show_reference_link;
	}
    
    
}
