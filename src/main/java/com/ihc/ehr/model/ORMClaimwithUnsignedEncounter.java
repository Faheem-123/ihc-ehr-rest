package com.ihc.ehr.model;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity
public class ORMClaimwithUnsignedEncounter{

	@Id
	private String row_id;
	private String total_count;
	private String chart_id;
	private String Claim_id;
	private String dos;
	private String chart_provider;
	private String visit_date;
	private String claim_date_created;
	private String patient_name;
	private String patient_id;

	
	
	 

	public String getTotal_count() {
		return total_count;
	}

	public void setTotal_count(String total_count) {
		this.total_count = total_count;
	}

	public String getRow_id() {
		return row_id;
	}

	public void setRow_id(String row_id) {
		this.row_id = row_id;
	}

	public String getChart_id() {
		return chart_id;
	}

	public void setChart_id(String chart_id) {
		this.chart_id = chart_id;
	}

	public String getClaim_id() {
		return Claim_id;
	}

	public void setClaim_id(String claim_id) {
		Claim_id = claim_id;
	}

	public String getDos() {
		return dos;
	}

	public void setDos(String dos) {
		this.dos = dos;
	}

	public String getChart_provider() {
		return chart_provider;
	}

	public void setChart_provider(String chart_provider) {
		this.chart_provider = chart_provider;
	}

	public String getVisit_date() {
		return visit_date;
	}

	public void setVisit_date(String visit_date) {
		this.visit_date = visit_date;
	}

	public String getClaim_date_created() {
		return claim_date_created;
	}

	public void setClaim_date_created(String claim_date_created) {
		this.claim_date_created = claim_date_created;
	}

	public String getPatient_name() {
		return patient_name;
	}

	public void setPatient_name(String patient_name) {
		this.patient_name = patient_name;
	}

	public String getPatient_id() {
		return patient_id;
	}

	public void setPatient_id(String patient_id) {
		this.patient_id = patient_id;
	}
}
