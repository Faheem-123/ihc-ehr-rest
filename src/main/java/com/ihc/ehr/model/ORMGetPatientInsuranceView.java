package com.ihc.ehr.model;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity
public class ORMGetPatientInsuranceView {

	@Id
	 private Long patientinsurance_id;
	    private String name;
	    private String address;
	    private String phone;
	    private String insurace_type;
	    private Long insurance_id;
	    private String elig_status;
	    
	    
	    
		public String getElig_status() {
			return elig_status;
		}
		public void setElig_status(String elig_status) {
			this.elig_status = elig_status;
		}
		public Long getPatientinsurance_id() {
			return patientinsurance_id;
		}
		public void setPatientinsurance_id(Long patientinsurance_id) {
			this.patientinsurance_id = patientinsurance_id;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public String getAddress() {
			return address;
		}
		public void setAddress(String address) {
			this.address = address;
		}
		public String getPhone() {
			return phone;
		}
		public void setPhone(String phone) {
			this.phone = phone;
		}
		public String getInsurace_type() {
			return insurace_type;
		}
		public void setInsurace_type(String insurace_type) {
			this.insurace_type = insurace_type;
		}
		public Long getInsurance_id() {
			return insurance_id;
		}
		public void setInsurance_id(Long insurance_id) {
			this.insurance_id = insurance_id;
		}
	    
}
