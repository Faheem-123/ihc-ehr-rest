package com.ihc.ehr.model;

import javax.persistence.Entity;
import javax.persistence.Id;

/**
 *
 * @author Ihsan Ullah
 */
@Entity
public class ORMGetClaimBatchOccurence {
    
    @Id
    private String occurence_code_id;
    private String claim_id;
    private String code;
    private String occurence_date;

    public String getOccurence_code_id() {
        return occurence_code_id;
    }

    public void setOccurence_code_id(String occurence_code_id) {
        this.occurence_code_id = occurence_code_id;
    }

    public String getClaim_id() {
        return claim_id;
    }

    public void setClaim_id(String claim_id) {
        this.claim_id = claim_id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getOccurence_date() {
        return occurence_date;
    }

    public void setOccurence_date(String occurence_date) {
        this.occurence_date = occurence_date;
    }
    
    
    
}
