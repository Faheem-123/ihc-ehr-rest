package com.ihc.ehr.model;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity
public class ORMLabResultLoincCode {

	 @Id
	    private String loinc_code;
	    private String shortname;
	    private String component;
	    private String loinc_system;
	    private String related_names;
		public String getLoinc_code() {
			return loinc_code;
		}
		public void setLoinc_code(String loinc_code) {
			this.loinc_code = loinc_code;
		}
		public String getShortname() {
			return shortname;
		}
		public void setShortname(String shortname) {
			this.shortname = shortname;
		}
		public String getComponent() {
			return component;
		}
		public void setComponent(String component) {
			this.component = component;
		}
		public String getLoinc_system() {
			return loinc_system;
		}
		public void setLoinc_system(String loinc_system) {
			this.loinc_system = loinc_system;
		}
		public String getRelated_names() {
			return related_names;
		}
		public void setRelated_names(String related_names) {
			this.related_names = related_names;
		}
	    
	    
}
