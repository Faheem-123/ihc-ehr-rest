package com.ihc.ehr.model;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity
public class ORMGetPatientConceptionOutcomesSummary {
	
	@Id
	private Long outcome_id;
	private String outcome_date;
	private String outcome;
	private String method;
	private String maternalcomments;
	private String sex;
	private String weight_lbs;
	private String weight_ozs;
	private String childhealthstatus;
	public Long getOutcome_id() {
		return outcome_id;
	}
	public void setOutcome_id(Long outcome_id) {
		this.outcome_id = outcome_id;
	}
	public String getOutcome_date() {
		return outcome_date;
	}
	public void setOutcome_date(String outcome_date) {
		this.outcome_date = outcome_date;
	}
	public String getOutcome() {
		return outcome;
	}
	public void setOutcome(String outcome) {
		this.outcome = outcome;
	}
	public String getMethod() {
		return method;
	}
	public void setMethod(String method) {
		this.method = method;
	}
	public String getMaternalcomments() {
		return maternalcomments;
	}
	public void setMaternalcomments(String maternalcomments) {
		this.maternalcomments = maternalcomments;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getWeight_lbs() {
		return weight_lbs;
	}
	public void setWeight_lbs(String weight_lbs) {
		this.weight_lbs = weight_lbs;
	}
	public String getWeight_ozs() {
		return weight_ozs;
	}
	public void setWeight_ozs(String weight_ozs) {
		this.weight_ozs = weight_ozs;
	}
	public String getChildhealthstatus() {
		return childhealthstatus;
	}
	public void setChildhealthstatus(String childhealthstatus) {
		this.childhealthstatus = childhealthstatus;
	}
	
	

}
