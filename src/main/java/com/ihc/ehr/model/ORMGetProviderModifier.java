package com.ihc.ehr.model;

import javax.persistence.Entity;
import javax.persistence.Id;
@Entity
public class ORMGetProviderModifier {
	  @Id
	     private String provider_modifier_id;
	    private String code;
	    private String description;
	    private String field;

	    public String getCode() {
	        return code;
	    }

	    public void setCode(String code) {
	        this.code = code;
	    }

	    public String getDescription() {
	        return description;
	    }

	    public void setDescription(String description) {
	        this.description = description;
	    }

	    public String getField() {
	        return field;
	    }

	    public void setField(String field) {
	        this.field = field;
	    }

	    public String getProvider_modifier_id() {
	        return provider_modifier_id;
	    }

	    public void setProvider_modifier_id(String provider_modifier_id) {
	        this.provider_modifier_id = provider_modifier_id;
	    }
}
