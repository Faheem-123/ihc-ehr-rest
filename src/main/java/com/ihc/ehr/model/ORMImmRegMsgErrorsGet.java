package com.ihc.ehr.model;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity
public class ORMImmRegMsgErrorsGet {

	@Id
    private Long error_id;	
    private Long message_id;
    private String severity;
    private String error_location;
    private String error_code;
    private String user_message;
	public Long getError_id() {
		return error_id;
	}
	public void setError_id(Long error_id) {
		this.error_id = error_id;
	}
	public Long getMessage_id() {
		return message_id;
	}
	public void setMessage_id(Long message_id) {
		this.message_id = message_id;
	}
	public String getSeverity() {
		return severity;
	}
	public void setSeverity(String severity) {
		this.severity = severity;
	}
	public String getError_location() {
		return error_location;
	}
	public void setError_location(String error_location) {
		this.error_location = error_location;
	}
	public String getError_code() {
		return error_code;
	}
	public void setError_code(String error_code) {
		this.error_code = error_code;
	}
	public String getUser_message() {
		return user_message;
	}
	public void setUser_message(String user_message) {
		this.user_message = user_message;
	}
    
    

}
