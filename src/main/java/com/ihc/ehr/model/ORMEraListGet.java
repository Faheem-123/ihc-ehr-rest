package com.ihc.ehr.model;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity
public class ORMEraListGet {

	@Id
	private Long era_id;
	private String check_amount;
	private String check_date;
	private String check_number;
	private String payer_name;

	private Boolean is_error;
	private Boolean posted;
	private Boolean is_processed;
	private String error_detail;
	
	private String file_path;

	public Long getEra_id() {
		return era_id;
	}

	public void setEra_id(Long era_id) {
		this.era_id = era_id;
	}

	public String getCheck_amount() {
		return check_amount;
	}

	public void setCheck_amount(String check_amount) {
		this.check_amount = check_amount;
	}

	public String getCheck_date() {
		return check_date;
	}

	public void setCheck_date(String check_date) {
		this.check_date = check_date;
	}

	public String getCheck_number() {
		return check_number;
	}

	public void setCheck_number(String check_number) {
		this.check_number = check_number;
	}

	public String getPayer_name() {
		return payer_name;
	}

	public void setPayer_name(String payer_name) {
		this.payer_name = payer_name;
	}

	public Boolean getIs_error() {
		return is_error;
	}

	public void setIs_error(Boolean is_error) {
		this.is_error = is_error;
	}

	public Boolean getPosted() {
		return posted;
	}

	public void setPosted(Boolean posted) {
		this.posted = posted;
	}

	public Boolean getIs_processed() {
		return is_processed;
	}

	public void setIs_processed(Boolean is_processed) {
		this.is_processed = is_processed;
	}

	public String getError_detail() {
		return error_detail;
	}

	public void setError_detail(String error_detail) {
		this.error_detail = error_detail;
	}
	
	

	public String getFile_path() {
		return file_path;
	}

	public void setFile_path(String file_path) {
		this.file_path = file_path;
	}

	@Override
	public String toString() {
		return "ORMEraListGet [era_id=" + era_id + ", check_amount=" + check_amount + ", check_date=" + check_date
				+ ", check_number=" + check_number + ", payer_name=" + payer_name + ", is_error=" + is_error
				+ ", posted=" + posted + ", is_processed=" + is_processed + ", error_detail=" + error_detail
				+ ", file_path=" + file_path + "]";
	}

	
	
}
