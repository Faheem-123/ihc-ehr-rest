package com.ihc.ehr.model;

import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "fax_attachment")
public class ORMFaxAttachmentSave {

	@Id
	private Long fax_attachment_id;
	private Long fax_sent_id;
	private Long patient_document_id;
	private String pages;
	private String doc_link;
	private String created_user;
	private String client_date_created;
	private String date_created;
	private String doc_name;
	private String fax_no;

	public Long getFax_attachment_id() {
		return fax_attachment_id;
	}

	public void setFax_attachment_id(Long fax_attachment_id) {
		this.fax_attachment_id = fax_attachment_id;
	}

	public Long getFax_sent_id() {
		return fax_sent_id;
	}

	public void setFax_sent_id(Long fax_sent_id) {
		this.fax_sent_id = fax_sent_id;
	}

	public Long getPatient_document_id() {
		return patient_document_id;
	}

	public void setPatient_document_id(Long patient_document_id) {
		this.patient_document_id = patient_document_id;
	}

	public String getPages() {
		return pages;
	}

	public void setPages(String pages) {
		this.pages = pages;
	}

	public String getDoc_link() {
		return doc_link;
	}

	public void setDoc_link(String doc_link) {
		this.doc_link = doc_link;
	}

	public String getCreated_user() {
		return created_user;
	}

	public void setCreated_user(String created_user) {
		this.created_user = created_user;
	}

	public String getClient_date_created() {
		return client_date_created;
	}

	public void setClient_date_created(String client_date_created) {
		this.client_date_created = client_date_created;
	}

	public String getDate_created() {
		return date_created;
	}

	public void setDate_created(String date_created) {
		this.date_created = date_created;
	}

	public String getDoc_name() {
		return doc_name;
	}

	public void setDoc_name(String doc_name) {
		this.doc_name = doc_name;
	}

	public String getFax_no() {
		return fax_no;
	}

	public void setFax_no(String fax_no) {
		this.fax_no = fax_no;
	}

	@Override
	public String toString() {
		return "ORMFaxAttachmentSave [fax_attachment_id=" + fax_attachment_id + ", fax_sent_id=" + fax_sent_id
				+ ", patient_document_id=" + patient_document_id + ", pages=" + pages + ", doc_link=" + doc_link
				+ ", created_user=" + created_user + ", client_date_created=" + client_date_created + ", date_created="
				+ date_created + ", doc_name=" + doc_name + ", fax_no=" + fax_no + "]";
	}

}
