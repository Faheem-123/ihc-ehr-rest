package com.ihc.ehr.model;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity
public class ORMChartAllergyView {

	@Id
	private String chart_allergies_id;
	private String patient_id;
	private String description;
	private String allergy_date;
	private String severity;
	private String concept_type;
	private String notes;
	private String onset_date;
	private String external_allergy_date;
	private String rxnorm;
	private String modified_user;
	private String status;
	private Boolean is_eallergy;	
	
	
	public String getModified_user() {
		return modified_user;
	}
	public void setModified_user(String modified_user) {
		this.modified_user = modified_user;
	}
	public String getChart_allergies_id() {
		return chart_allergies_id;
	}
	public void setChart_allergies_id(String chart_allergies_id) {
		this.chart_allergies_id = chart_allergies_id;
	}
	public String getPatient_id() {
		return patient_id;
	}
	public void setPatient_id(String patient_id) {
		this.patient_id = patient_id;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getAllergy_date() {
		return allergy_date;
	}
	public void setAllergy_date(String allergy_date) {
		this.allergy_date = allergy_date;
	}
	public String getSeverity() {
		return severity;
	}
	public void setSeverity(String severity) {
		this.severity = severity;
	}
	public String getConcept_type() {
		return concept_type;
	}
	public void setConcept_type(String concept_type) {
		this.concept_type = concept_type;
	}
	public String getNotes() {
		return notes;
	}
	public void setNotes(String notes) {
		this.notes = notes;
	}
	public String getOnset_date() {
		return onset_date;
	}
	public void setOnset_date(String onset_date) {
		this.onset_date = onset_date;
	}
	public String getExternal_allergy_date() {
		return external_allergy_date;
	}
	public void setExternal_allergy_date(String external_allergy_date) {
		this.external_allergy_date = external_allergy_date;
	}
	public String getRxnorm() {
		return rxnorm;
	}
	public void setRxnorm(String rxnorm) {
		this.rxnorm = rxnorm;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public Boolean getIs_eallergy() {
		return is_eallergy;
	}
	public void setIs_eallergy(Boolean is_eallergy) {
		this.is_eallergy = is_eallergy;
	}
	
	
	
}
