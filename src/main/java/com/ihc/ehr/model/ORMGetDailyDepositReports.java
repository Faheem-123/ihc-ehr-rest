package com.ihc.ehr.model;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity
public class ORMGetDailyDepositReports {
	@Id
    private String dailydeposit_id;
    private String check_number;
    private String check_date;
    private String check_amount;
    private String insurance_name;
    private String comments;
    private String created_user;
    private String date_created;
    private String original_file_name;
    private String file_link  ;
    private Boolean eob_era_mapped;
    private String check_received_date;
	public String getDailydeposit_id() {
		return dailydeposit_id;
	}
	public void setDailydeposit_id(String dailydeposit_id) {
		this.dailydeposit_id = dailydeposit_id;
	}
	public String getCheck_number() {
		return check_number;
	}
	public void setCheck_number(String check_number) {
		this.check_number = check_number;
	}
	public String getCheck_date() {
		return check_date;
	}
	public void setCheck_date(String check_date) {
		this.check_date = check_date;
	}
	public String getCheck_amount() {
		return check_amount;
	}
	public void setCheck_amount(String check_amount) {
		this.check_amount = check_amount;
	}
	public String getInsurance_name() {
		return insurance_name;
	}
	public void setInsurance_name(String insurance_name) {
		this.insurance_name = insurance_name;
	}
	public String getComments() {
		return comments;
	}
	public void setComments(String comments) {
		this.comments = comments;
	}
	public String getCreated_user() {
		return created_user;
	}
	public void setCreated_user(String created_user) {
		this.created_user = created_user;
	}
	public String getDate_created() {
		return date_created;
	}
	public void setDate_created(String date_created) {
		this.date_created = date_created;
	}
	public String getOriginal_file_name() {
		return original_file_name;
	}
	public void setOriginal_file_name(String original_file_name) {
		this.original_file_name = original_file_name;
	}
	public String getFile_link() {
		return file_link;
	}
	public void setFile_link(String file_link) {
		this.file_link = file_link;
	}
	public Boolean getEob_era_mapped() {
		return eob_era_mapped;
	}
	public void setEob_era_mapped(Boolean eob_era_mapped) {
		this.eob_era_mapped = eob_era_mapped;
	}
	public String getCheck_received_date() {
		return check_received_date;
	}
	public void setCheck_received_date(String check_received_date) {
		this.check_received_date = check_received_date;
	}
    
}
