package com.ihc.ehr.model;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity
public class ORMGetPaymentCollectionSummaryReport {

	 @Id
	    private String s_no;
	    private String month_name;
	    private String payment_type;
	    private String received_amount;
	    private String posted_from_received;
	    private String total_posted;
		public String getS_no() {
			return s_no;
		}
		public void setS_no(String s_no) {
			this.s_no = s_no;
		}
		public String getMonth_name() {
			return month_name;
		}
		public void setMonth_name(String month_name) {
			this.month_name = month_name;
		}
		public String getPayment_type() {
			return payment_type;
		}
		public void setPayment_type(String payment_type) {
			this.payment_type = payment_type;
		}
		public String getReceived_amount() {
			return received_amount;
		}
		public void setReceived_amount(String received_amount) {
			this.received_amount = received_amount;
		}
		public String getPosted_from_received() {
			return posted_from_received;
		}
		public void setPosted_from_received(String posted_from_received) {
			this.posted_from_received = posted_from_received;
		}
		public String getTotal_posted() {
			return total_posted;
		}
		public void setTotal_posted(String total_posted) {
			this.total_posted = total_posted;
		}
	    
	    
}
