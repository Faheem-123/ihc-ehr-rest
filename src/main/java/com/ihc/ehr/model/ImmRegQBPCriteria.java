package com.ihc.ehr.model;

public class ImmRegQBPCriteria {
	
	private Long practice_id;
	private String clinic_id;
	private String registry_code;	
	private String user_name;
	private String client_ip;
	private Long patient_id;	
	private boolean submit_after_geneation;	
	
	
	public Long getPractice_id() {
		return practice_id;
	}
	public void setPractice_id(Long practice_id) {
		this.practice_id = practice_id;
	}
	public String getClinic_id() {
		return clinic_id;
	}
	public void setClinic_id(String clinic_id) {
		this.clinic_id = clinic_id;
	}
	public String getRegistry_code() {
		return registry_code;
	}
	public void setRegistry_code(String registry_code) {
		this.registry_code = registry_code;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getClient_ip() {
		return client_ip;
	}
	public void setClient_ip(String client_ip) {
		this.client_ip = client_ip;
	}
	public Long getPatient_id() {
		return patient_id;
	}
	public void setPatient_id(Long patient_id) {
		this.patient_id = patient_id;
	}
	public boolean isSubmit_after_geneation() {
		return submit_after_geneation;
	}
	public void setSubmit_after_geneation(boolean submit_after_geneation) {
		this.submit_after_geneation = submit_after_geneation;
	}	


	
}
