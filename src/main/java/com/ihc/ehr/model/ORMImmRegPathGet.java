package com.ihc.ehr.model;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity
public class ORMImmRegPathGet {
	
	@Id
	private Integer id;
	private String registry_code;
	private String outgoing_path;
	private String acknowledge_path;
	private String processed_path;
	private String response_path;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getRegistry_code() {
		return registry_code;
	}
	public void setRegistry_code(String registry_code) {
		this.registry_code = registry_code;
	}
	public String getOutgoing_path() {
		return outgoing_path;
	}
	public void setOutgoing_path(String outgoing_path) {
		this.outgoing_path = outgoing_path;
	}
	public String getAcknowledge_path() {
		return acknowledge_path;
	}
	public void setAcknowledge_path(String acknowledge_path) {
		this.acknowledge_path = acknowledge_path;
	}
	public String getProcessed_path() {
		return processed_path;
	}
	public void setProcessed_path(String processed_path) {
		this.processed_path = processed_path;
	}
	public String getResponse_path() {
		return response_path;
	}
	public void setResponse_path(String response_path) {
		this.response_path = response_path;
	}	
}
