package com.ihc.ehr.model;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity
public class ORMLabFollowUpPatientList {

	  @Id
	    private String patient_id;
	    private String patient_display_name;
	    private Long notes_aging;
	    private String cell_phone;
	    private String home_phone;
	    private String work_phone;
	    private String primary_contact_type;
	    private String gender;
	    private String age;
	    private String patient_status;
	    private String patient_language;
		public String getPatient_id() {
			return patient_id;
		}
		public void setPatient_id(String patient_id) {
			this.patient_id = patient_id;
		}
		public String getPatient_display_name() {
			return patient_display_name;
		}
		public void setPatient_display_name(String patient_display_name) {
			this.patient_display_name = patient_display_name;
		}
		public Long getNotes_aging() {
			return notes_aging;
		}
		public void setNotes_aging(Long notes_aging) {
			this.notes_aging = notes_aging;
		}
		public String getCell_phone() {
			return cell_phone;
		}
		public void setCell_phone(String cell_phone) {
			this.cell_phone = cell_phone;
		}
		public String getHome_phone() {
			return home_phone;
		}
		public void setHome_phone(String home_phone) {
			this.home_phone = home_phone;
		}
		public String getWork_phone() {
			return work_phone;
		}
		public void setWork_phone(String work_phone) {
			this.work_phone = work_phone;
		}
		public String getPrimary_contact_type() {
			return primary_contact_type;
		}
		public void setPrimary_contact_type(String primary_contact_type) {
			this.primary_contact_type = primary_contact_type;
		}
		public String getGender() {
			return gender;
		}
		public void setGender(String gender) {
			this.gender = gender;
		}
		public String getAge() {
			return age;
		}
		public void setAge(String age) {
			this.age = age;
		}
		public String getPatient_status() {
			return patient_status;
		}
		public void setPatient_status(String patient_status) {
			this.patient_status = patient_status;
		}
		public String getPatient_language() {
			return patient_language;
		}
		public void setPatient_language(String patient_language) {
			this.patient_language = patient_language;
		}
	    
	    
	    
}
