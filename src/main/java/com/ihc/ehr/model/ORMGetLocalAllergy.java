package com.ihc.ehr.model;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity
public class ORMGetLocalAllergy {
	
	@Id
	private Long chart_allergies_id;
	private String description;
	private String alergy_date;
	private String onset_date;
	private String severity;
	private String notes;
	private String status;
	private String created_user;
	private String date_created;
	private String client_date_created;
	private Long patient_id;
	public Long getChart_allergies_id() {
		return chart_allergies_id;
	}
	public void setChart_allergies_id(Long chart_allergies_id) {
		this.chart_allergies_id = chart_allergies_id;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getAlergy_date() {
		return alergy_date;
	}
	public void setAlergy_date(String alergy_date) {
		this.alergy_date = alergy_date;
	}
	public String getOnset_date() {
		return onset_date;
	}
	public void setOnset_date(String onset_date) {
		this.onset_date = onset_date;
	}
	public String getSeverity() {
		return severity;
	}
	public void setSeverity(String severity) {
		this.severity = severity;
	}
	public String getNotes() {
		return notes;
	}
	public void setNotes(String notes) {
		this.notes = notes;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getCreated_user() {
		return created_user;
	}
	public void setCreated_user(String created_user) {
		this.created_user = created_user;
	}
	public String getDate_created() {
		return date_created;
	}
	public void setDate_created(String date_created) {
		this.date_created = date_created;
	}
	public String getClient_date_created() {
		return client_date_created;
	}
	public void setClient_date_created(String client_date_created) {
		this.client_date_created = client_date_created;
	}
	public Long getPatient_id() {
		return patient_id;
	}
	public void setPatient_id(Long patient_id) {
		this.patient_id = patient_id;
	}
	
	
	

}
